add = (n) => n + n

divide = (n) => n / n

subtract = (x, y) => x - y

multiply = (n) => n * n

console.log(add(40))
console.log(divide(50))
console.log(subtract(78, 39))
console.log(multiply(28))


function mathOperations(n1, n2, n3, n4){
    return n1 / n2 * (n3 + n4)
}

